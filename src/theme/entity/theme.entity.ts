import { GradeEntity } from "src/grade/entity/grade.entity";
import { PatternsEntity } from "src/question-generator/entity/patterns.entity";
import { QuestionEntity } from "src/question/entity/question.entity";
import { UserEntity } from "src/user/entity/user.entity";
import { Column, CreateDateColumn, Entity, OneToMany, OneToOne, PrimaryGeneratedColumn, UpdateDateColumn } from "typeorm";

@Entity({name: 'Theme'})
export class ThemeEntity{
    @PrimaryGeneratedColumn('increment')
    id: string;

    @Column({unique: true})
    title: string; 

    @Column({name: 'max_points', type: 'int'})
    maxPoints: number;

    @Column({name: 'can_be_auto_generated', type: 'boolean', default: false})
    isAutoGenerated: boolean;

    @OneToMany(() => PatternsEntity, (pattern: PatternsEntity) => pattern.theme, {cascade: true})
    generationPatterns: PatternsEntity[];
    
    @OneToMany(() => QuestionEntity, (question: QuestionEntity) => question.theme, {cascade: true})
    questions: QuestionEntity[];

    @OneToMany(() => GradeEntity, (grade: GradeEntity) => grade.theme, {cascade: true})
    grade: GradeEntity;

    @OneToMany(() => UserEntity, (user) => user.chosenTheme)
    activeUsers: UserEntity[];

    @CreateDateColumn()
    createdAt: Date;

    @UpdateDateColumn()
    updatedAt: Date;
}//Остановился тут, нужно посмотреть правильное удаление таблицы grade через cascade